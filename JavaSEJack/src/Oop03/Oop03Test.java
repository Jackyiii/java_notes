package Oop03;

public class Oop03Test {
	//基本数据类型
	byte b;   //0
	short s;  //0
	int i;    //0
	char c;   //u0000
	long l;   //0
	float f;  //0.0 
	double d; //0.0
	boolean bool; //false
	String str;
	public static void main(String[] args) {
		//局部变量
		Oop03Test var=new Oop03Test();
		System.out.println(var.b);
		System.out.println(var.s);
		System.out.println(var.i);
		System.out.println(var.c);
		System.out.println(var.l);
		System.out.println(var.f);
		System.out.println(var.d);
		System.out.println(var.bool);
		System.out.println(var.str);
	}

}
